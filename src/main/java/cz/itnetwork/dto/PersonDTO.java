package cz.itnetwork.dto;

import com.fasterxml.jackson.annotation.JsonProperty;
import cz.itnetwork.constant.Countries;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * This class represents the DTO of a person (or company), that can be later designated as buyer or seller for an invoice
 * Getters, setters, constructors generated with lombok
 *
 * @author Itnetwork.cz
 * @author The author of javadoc for this class is Kat
 */
@Data
@AllArgsConstructor
@NoArgsConstructor
public class PersonDTO {

    /**
     * <p>Represents the id of a person/company</p>
     * <p>Generated by the database in {@link cz.itnetwork.entity.PersonEntity}</p>
     * <p>Connects to json property sent from client via {@code @JsonProperty("_id)}</p>
     */
    @JsonProperty("_id")
    private Long id;

    /**
     * <p>Represents the name of the person/company</p>
     * <p>Entered by user</p>
     */
    private String name;

    /**
     * <p>Represents the identification number of a person/company (IČO in Czechia)</p>
     * <p>Entered by user</p>
     */
    private String identificationNumber;

    /**
     * <p>Represents the tax number of a person/company (DIČ in Czechia)</p>
     * <p>Entered by user</p>
     */
    private String taxNumber;

    /**
     * <p>Represents the account number of a person/company</p>
     * <p>Entered by user</p>
     */
    private String accountNumber;

    /**
     * <p>Represents the code of a bank</p>
     * <p>Entered by user</p>
     */
    private String bankCode;

    /**
     * <p>Represents the International Bank Account Number of a person/company</p>
     * <p>Entered by user</p>
     */
    private String iban;

    /**
     * <p>Represents the telephone number of a person/company</p>
     * <p>Entered by user</p>
     */
    private String telephone;

    /**
     * <p>Represents the e-mail of a person/company</p>
     * <p>Entered by user</p>
     */
    private String mail;

    /**
     * <p>Represents the street of the headquarters of a person/company</p>
     * <p>Entered by user</p>
     */
    private String street;

    /**
     * <p>Represents the zip code of the headquarters of a person/company</p>
     * <p>Entered by user</p>
     */
    private String zip;

    /**
     * <p>Represents the city, where the headquarters of a person/company is located</p>
     * <p>Entered by user</p>
     */
    private String city;

    /**
     * <p>Represents the country, where the headquarters of a person/company is located</p>
     * <p>Must be chosen from enum {@link Countries}</p>
     * <p>Entered by user</p>
     */
    private Countries country;

    /**
     * <p>Represents a note to a person/company</p>
     * <p>It is mandatory</p>
     */
    private String note;
}
